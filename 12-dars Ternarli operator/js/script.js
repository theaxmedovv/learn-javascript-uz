// Ternarli operator

// bu Ternarli operator shunday ishlaydi
// shart ? to'g'ri : noto'g'ri
const age = 18;
age >= 18 ? console.log("Siz kattasiz") : console.log("Siz kichkinsiz");

// Ternarli operatorni if else bilan solishtirish
if (age >= 18) {
  console.log("Siz kattasiz");
} else {
  console.log("Siz kichkinsiz");
}