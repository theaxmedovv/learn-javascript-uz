// Decrement esa har doim o'zgaruvchining qiymatini 1 ga kamaytiradi
// Increment esa har doim o'zgaruvchining qiymatini 1 ga oshiradi

let count = 0;

count++; // bu bittaga oshiradi
console.log(count); // 1

count--; // u bittaga kamaytiradi
console.log(count); // 0




// Decrement va Increment operatorlari yana  Postfix va Prefix shaklida bo'lishi mumkin
// Postfix - o'zgaruvchining qiymatini keyingi qator bajarilgandan keyin oshiradi yoki kamaytiradi
// Prefix - o'zgaruvchining qiymatini shu qator bajarilishidan oldin oshiradi yoki kamaytiradi

let number = 5;
console.log(--number); // 4 (shu qator bajarilishidan oldin kamaytiradi)
console.log(number++); // 4 (keyingi qator bajarilgandan keyin oshiradi)
console.log(number);   // 5
console.log(++number); // 6 (shu qator bajarilishidan oldin oshiradi)


